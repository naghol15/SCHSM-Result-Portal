function checkResult() {

    const rollNo = document.getElementById("rollInput").value.trim();
    const resultDiv = document.getElementById("result");
    resultDiv.innerHTML = "";

    const student = students.find(
        s => s["ROLL  NO."] === rollNo
    );

    if (!student) {
        resultDiv.innerHTML = "<p style='color:red'>Result Not Found</p>";
        return;
    }

    let html = `
        <h3 style="text-align:center">Marksheet</h3>
        <p><b>Name:</b> ${student["STUDENT NAME"]}</p>
        <p><b>Father Name:</b> ${student["FATHER'S NAME"]}</p>
        <p><b>Class:</b> ${student["CLASS"]}</p>
        <p><b>Roll No:</b> ${student["ROLL  NO."]}</p>

        <table>
            <tr><th>Subject</th><th>Marks</th></tr>
    `;

    /* AUTO SUBJECT DETECTION */
    const ignoreKeys = [
        "ROLL  NO.",
        "STUDENT NAME",
        "FATHER'S NAME",
        "CLASS",
        "Total Marks",
        "Obtained Marks",
        "Percentage",
        "Grade",
        "Result"
    ];

    for (let key in student) {
        if (!ignoreKeys.includes(key)) {
            html += `<tr><td>${key}</td><td>${student[key]}</td></tr>`;
        }
    }

    html += `
        </table>
        <p><b>Total Marks:</b> ${student["Total Marks"]}</p>
        <p><b>Obtained:</b> ${student["Obtained Marks"]}</p>
        <p><b>Percentage:</b> ${student["Percentage"]}%</p>
        <p><b>Grade:</b> ${student["Grade"]}</p>
        <p><b>Result:</b> ${student["Result"]}</p>
    `;

    resultDiv.innerHTML = html;
}
